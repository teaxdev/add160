class AddressBook():
    def __init__(self, first_name, last_name, birthday, email,
                   street_address, city, state, zip, phone):
        self.first_name = first_name
        self.last_name = last_name
        self.birthday = birthday
        self.email = email
        self.street_address = street_address
        self.city = city
        self.state = state
        self.zip = zip
        self.phone = phone

    